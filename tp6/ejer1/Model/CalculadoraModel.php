<?php

class Calculadora {


    function suma($num1,$num2){

        $resultado = $num1 + $num2;
        return $resultado;

    }
    function restar($num1,$num2){

        $resultado = $num1 - $num2;
        return $resultado;

    }
    function dividir($num1,$num2){

        $resultado = $num1 / $num2;
        return $resultado;

    }
    function multiplicar($num1,$num2){

        $resultado = $num1 * $num2;
        return $resultado;

    }


    
}